package monopoly.divers;

public class ProprieteDejaPossedeeException extends Exception {

	private static final long serialVersionUID = 1L;

	public ProprieteDejaPossedeeException() {
		super();
	}

	public ProprieteDejaPossedeeException(String message) {
		super(message);

	}

	public ProprieteDejaPossedeeException(Throwable cause) {
		super(cause);
	}

	public ProprieteDejaPossedeeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProprieteDejaPossedeeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
